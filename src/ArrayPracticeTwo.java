public class ArrayPracticeTwo {

    public static void main(String[] args) {

        String[] myArray = new String[3];

        myArray[0] = "Hej";
        myArray[1] = "med";
        myArray[2] = "dig";

        for (String i : myArray) {
            System.out.print(i + " ");
        }

    }
}
